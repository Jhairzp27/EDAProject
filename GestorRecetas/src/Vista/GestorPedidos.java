    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        String nombreCliente = jTextField1.getText();
        String descripcion = jTextField2.getText();
        Pedido nuevoPedido = new Pedido(nombreCliente, descripcion);
        colaPedidos.agregarPedido(nuevoPedido);
        jTextField1.setText("");
        jTextField2.setText("");
    }//GEN-LAST:event_botonAgregarActionPerformed

    private void botonProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonProcesarActionPerformed
        Pedido pedidoProcesado = colaPedidos.procesarPedido();
        if (pedidoProcesado != null) {
            JOptionPane.showMessageDialog(this, "Pedido procesado:\n" + pedidoProcesado.toString());
        }
    }//GEN-LAST:event_botonProcesarActionPerformed

